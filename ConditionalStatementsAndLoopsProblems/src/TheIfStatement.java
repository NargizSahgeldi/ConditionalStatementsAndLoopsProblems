import java.util.Scanner;

public class TheIfStatement {
    public static void main(String[] args) {
        solve();
    }

    public static void solve() {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();

        if (n > 100) {
            System.out.println("Big");
        }
            System.out.println("Number");
    }
}
